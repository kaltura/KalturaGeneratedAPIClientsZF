/**
 * @package Kaltura
 * @subpackage Client
 */
class Kaltura_Client_Sso_Type_Sso extends Kaltura_Client_ObjectBase
{
	public function getKalturaObjectType()
	{
		return 'KalturaSso';
	}
	
	public function __construct(SimpleXMLElement $xml = null, $jsonObject = null)
	{
		parent::__construct($xml, $jsonObject);
		
		if(!is_null($xml) && !is_null($jsonObject))
			throw new Kaltura_Client_ClientException("construct with either XML or JSON object, not both", Kaltura_Client_ClientException::ERROR_CONSTRUCT_ARGS_CONFLICT);
		
		if(is_null($xml) && is_null($jsonObject))
			return;
		
		if(!is_null($xml) && count($xml->id))
			$this->id = (int)$xml->id;
		if(!is_null($jsonObject) && isset($jsonObject->id))
			$this->id = (int)$jsonObject->id;
		if(!is_null($xml) && count($xml->applicationType))
			$this->applicationType = (string)$xml->applicationType;
		if(!is_null($jsonObject) && isset($jsonObject->applicationType))
			$this->applicationType = (string)$jsonObject->applicationType;
		if(!is_null($xml) && count($xml->partnerId))
			$this->partnerId = (int)$xml->partnerId;
		if(!is_null($jsonObject) && isset($jsonObject->partnerId))
			$this->partnerId = (int)$jsonObject->partnerId;
		if(!is_null($xml) && count($xml->domain))
			$this->domain = (string)$xml->domain;
		if(!is_null($jsonObject) && isset($jsonObject->domain))
			$this->domain = (string)$jsonObject->domain;
		if(!is_null($xml) && count($xml->status))
			$this->status = (int)$xml->status;
		if(!is_null($jsonObject) && isset($jsonObject->status))
			$this->status = (int)$jsonObject->status;
		if(!is_null($xml) && count($xml->createdAt))
			$this->createdAt = (int)$xml->createdAt;
		if(!is_null($jsonObject) && isset($jsonObject->createdAt))
			$this->createdAt = (int)$jsonObject->createdAt;
		if(!is_null($xml) && count($xml->updatedAt))
			$this->updatedAt = (int)$xml->updatedAt;
		if(!is_null($jsonObject) && isset($jsonObject->updatedAt))
			$this->updatedAt = (int)$jsonObject->updatedAt;
		if(!is_null($xml) && count($xml->redirectUrl))
			$this->redirectUrl = (string)$xml->redirectUrl;
		if(!is_null($jsonObject) && isset($jsonObject->redirectUrl))
			$this->redirectUrl = (string)$jsonObject->redirectUrl;
		if(!is_null($xml) && count($xml->data))
			$this->data = (string)$xml->data;
		if(!is_null($jsonObject) && isset($jsonObject->data))
			$this->data = (string)$jsonObject->data;
	}
	/**
	 * 
	 *
	 * @var int
	 * @readonly
	 */
	public $id = null;

	/**
	 * 
	 *
	 * @var string
	 */
	public $applicationType = null;

	/**
	 * 
	 *
	 * @var int
	 * @insertonly
	 */
	public $partnerId = null;

	/**
	 * 
	 *
	 * @var string
	 */
	public $domain = null;

	/**
	 * 
	 *
	 * @var Kaltura_Client_Sso_Enum_SsoStatus
	 */
	public $status = null;

	/**
	 * Creation date as Unix timestamp (In seconds)
	 *
	 * @var int
	 * @readonly
	 */
	public $createdAt = null;

	/**
	 * Last update date as Unix timestamp (In seconds)
	 *
	 * @var int
	 * @readonly
	 */
	public $updatedAt = null;

	/**
	 * Redirect URL for a specific application type and (partner id or domain)
	 *
	 * @var string
	 */
	public $redirectUrl = null;

	/**
	 * 
	 *
	 * @var string
	 */
	public $data = null;


}

